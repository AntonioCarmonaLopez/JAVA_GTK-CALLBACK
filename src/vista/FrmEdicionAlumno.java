   public void mostrar(ImantenimientoCallback imc) {
        this.setLocationRelativeTo(null);
//se configura el listener
        this.imc = imc;

        if (!FrmMantenimientoAlumno.editar) {
            this.setName("Alta Alumno");
            this.setTitle("AltaAlumnos");
        } else {
            this.setName("Editar Alumno");
            this.setTitle("Editar Alumnos");
            this.txtDni.setEditable(false);
            this.txtDni.setBackground(Color.lightGray);
            txtDni.setText(FrmMantenimientoAlumno.dni);
            txtNombre.setText(FrmMantenimientoAlumno.nombre);
            if(FrmMantenimientoAlumno.mayor.equalsIgnoreCase("true"))
                chkMayor.setSelected(true);
        }

        this.setVisible(true);
    }

    private boolean datosObligatorios() {
        return txtDni.getText().isEmpty() || txtNombre.getText().isEmpty();
    }

    private void comprobarDatos() {
        if (datosObligatorios()) {
            btnAceptar.setEnabled(false);
        } else {
            btnAceptar.setEnabled(true);
        }
    }

