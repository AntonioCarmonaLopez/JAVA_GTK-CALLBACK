    private void btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionPerformed
        if (!FrmMantenimientoAlumno.editar) {
            if (evt.getSource() == btnAceptar) {
                if (datosObligatorios()) {
                    JOptionPane.showMessageDialog(this,
                            "Faltan Datos Obligatorios",
                            this.getName(),
                            JOptionPane.WARNING_MESSAGE);
                    return;
                }
                try {

                    Alumno a = new Alumno();
                    a.setDni(txtDni.getText());
                    a.setNombre(txtNombre.getText());
                    a.setMayorEdad(chkMayor.isSelected());
                    LogicaAlumno.altaAlumno(a);

                    JOptionPane.showConfirmDialog(this,
                            "Alta Alumno Correcto",
                            this.getName(),
                            JOptionPane.PLAIN_MESSAGE);

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this,
                            "Alta Alumno Incorrecta",
                            this.getName(),
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            mantenimiento=true;
            if (evt.getSource() == btnAceptar) {
                if (datosObligatorios()) {
                    JOptionPane.showMessageDialog(this,
                            "Faltan Datos Obligatorios",
                            this.getName(),
                            JOptionPane.WARNING_MESSAGE);
                    return;
                }
                try {

                    Alumno a = new Alumno();
                    a.setDni(txtDni.getText());
                    a.setNombre(txtNombre.getText());
                    a.setMayorEdad(chkMayor.isSelected());

                    LogicaAlumno.actualizaAlumno(a);

                    JOptionPane.showMessageDialog(this,
                            "Alumno Actualizado",
                            this.getName(),
                            JOptionPane.PLAIN_MESSAGE);

                } catch (Exception e) {
                    JOptionPane.showConfirmDialog(this,
                            "Actualizacion Incorrecta",
                            this.getName(),
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        imc.alctualizaMto();

        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_btnActionPerformed

    private void txtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (evt.getSource() == txtDni) {
                txtNombre.requestFocus();
            }else if(evt.getSource()==txtNombre){
                btnAceptar.requestFocus();
                btnAceptar.doClick();
            }else{
                btnCancelar.requestFocus();
                btnCancelar.doClick();
            }
        }
    }//GEN-LAST:event_txtKeyPressed

