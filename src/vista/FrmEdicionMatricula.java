    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if (datosObligatorios()) {
            JOptionPane.showMessageDialog(this,
                    "Faltan Datos Obligatorios",
                    this.getName(),
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {

            Matricula m = new Matricula();
            m.setIdCurso(Integer.parseInt(txtId.getText()));
            m.setDniAlumno(txtDni.getText());

            LogicaMatricula.altaMatricula(m);

            JOptionPane.showConfirmDialog(this,
                    "Alta Matricula Correcta",
                    this.getName(),
                    JOptionPane.PLAIN_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Alta Matricula Incorrecta",
                    this.getName(),
                    JOptionPane.ERROR_MESSAGE);
        }
        imc.alctualizaMto();
        btnAceptar.setEnabled(true);
        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_btnAceptarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (evt.getSource() == btnBuscarCurso) {
            FrmEdicionCurso.mantenimiento = true;
            FrmMantenimientoCurso frameMC = new FrmMantenimientoCurso();
            frameMC.setSize(790, 540);
            frameMC.setVisible(false);
            frameMC.mostrar(this);
        } else {
            FrmEdicionCurso.mantenimiento = true;
            FrmMantenimientoAlumno frameMP = new FrmMantenimientoAlumno();
            frameMP.setSize(790, 540);
            frameMP.setVisible(false);
            frameMP.mostrar(this);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

