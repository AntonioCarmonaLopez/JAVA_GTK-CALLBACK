    @Override
    public void actualizarMatricula() {

        if (FrmMantenimientoCurso.mToCurso) {
            txtId.setText(Integer.toString(FrmMantenimientoCurso.id));
            txtNombreCurso.setText(FrmMantenimientoCurso.nombre);
            FrmMantenimientoCurso.mToCurso = false;
        }
        if (FrmMantenimientoAlumno.mToAlumno) {
            txtDni.setText(FrmMantenimientoAlumno.dni);
            txtNomAlumno.setText(FrmMantenimientoAlumno.nombre);
            FrmMantenimientoAlumno.mToAlumno = false;
        }
        FrmEdicionCurso.mantenimiento = false;
    }
