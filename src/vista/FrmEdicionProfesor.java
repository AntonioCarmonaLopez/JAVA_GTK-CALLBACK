    public void mostrar(ImantenimientoCallback imc) {
        this.setLocationRelativeTo(null);
//se configura el listener
        this.imc = imc;

        if (!FrmMantenimientoProfesores.editar) {
            this.setName("Alta Profesor");
            this.setTitle("Alta Profesores");
        } else {
            this.setName("Editar Profesor");
            this.setTitle("Editar Profesores");
            this.txtDni.setEditable(false);
            this.txtDni.setBackground(Color.lightGray);
            txtDni.setText(FrmMantenimientoProfesores.dni);
            txtNombre.setText(FrmMantenimientoProfesores.nombre);
        }

        this.setVisible(true);
    }

    private boolean datosObligatorios() {
        return txtNombre.getText().isEmpty();
    }

    private void comprobarDatos() {
        if (datosObligatorios()) {
            btnAceptar.setEnabled(false);
        } else {
            btnAceptar.setEnabled(true);
        }
    }

