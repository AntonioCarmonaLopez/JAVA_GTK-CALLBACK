    private void btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionPerformed

        if (!FrmMantenimientoProfesores.editar) {
            if (evt.getSource() == btnAceptar) {
                if (datosObligatorios()) {
                    JOptionPane.showMessageDialog(this,
                            "Faltan Datos Obligatorios",
                            this.getName(),
                            JOptionPane.WARNING_MESSAGE);
                    return;
                }
                try {

                    Profesor p = new Profesor();
                    p.setDni(txtDni.getText());
                    p.setNombre(txtNombre.getText());

                    LogicaProfesor.altaProfesor(p);

                    JOptionPane.showConfirmDialog(this,
                            "Alta Profesor Correcto",
                            this.getName(),
                            JOptionPane.PLAIN_MESSAGE);

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this,
                            "Alta Profesor Incorrecta",
                            this.getName(),
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {

            if (evt.getSource() == btnAceptar) {
                if (datosObligatorios()) {
                    JOptionPane.showMessageDialog(this,
                            "Faltan Datos Obligatorios",
                            this.getName(),
                            JOptionPane.WARNING_MESSAGE);
                    return;
                }
                try {

                    Profesor p = new Profesor();
                    p.setDni(txtDni.getText());
                    p.setNombre(txtNombre.getText());

                    LogicaProfesor.actualizaProfesor(p);

                    JOptionPane.showMessageDialog(this,
                            "Profesor Actualizado",
                            this.getName(),
                            JOptionPane.PLAIN_MESSAGE);

                } catch (Exception e) {
                    JOptionPane.showConfirmDialog(this,
                            "Actualizacion Incorrecta",
                            this.getName(),
                            JOptionPane.ERROR_MESSAGE);
                }
            }

        }
        imc.alctualizaMto();

        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_btnActionPerformed

    private void txtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKeyPressed
         if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (evt.getSource() == txtDni) {
                txtNombre.requestFocus();
            }else if(evt.getSource()==txtNombre){
                btnAceptar.requestFocus();
                btnAceptar.doClick();
            }else{
                btnCancelar.requestFocus();
                btnCancelar.doClick();
            }
        }
    }//GEN-LAST:event_txtKeyPressed

